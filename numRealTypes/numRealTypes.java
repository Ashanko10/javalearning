package numRealTypes;

public class numRealTypes {
  public static void main(String[] args) {
    float f1 = (float)50;
    System.out.println(f1);

    float f2 = 50F; 
    System.out.println(f2);

    float f3 = 50f; 
    System.out.println(f3);

    float f4 = 50/3;
    System.out.println(f4);
    // (float), F, F, pembagian hasil koma

    double d1 = (double)50;
    System.out.println(d1);

    double d2 = 50D;
    System.out.println(d2);

    double d3 = 50d; 
    System.out.println(d3);

    double d4 = 50/3;
    System.out.println(d4);
    // (double), D, d, pembagian hasil koma
  }
  
}
